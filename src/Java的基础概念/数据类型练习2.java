package Java的基础概念;

public class 数据类型练习2 {
    public static void main(String[] args) {
        //定义变量记录电影的名字
        String name = "送初恋回家";
        //定义主演名字
        String tostar = "刘星 张鱼体 高院";
        //定义电影的年份
        int year = 2020;
        //定义记录电影的评分
        double grade = 9.0;

        //输出所有变量的值
        System.out.println(name);
        System.out.println(tostar);
        System.out.println(year);
        System.out.println(grade);
    }
}
