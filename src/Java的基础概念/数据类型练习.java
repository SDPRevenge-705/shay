package Java的基础概念;

public class 数据类型练习 {
    public static void main(String[] args) {
        //定义变量记录姓名
        String name = "黑马楚";
        //定义年龄
        int age = 18;
        //定义性别
        char gender = '男';
        //定义身高
        double height = 180.1;
        //定义是否单身
        //true单身 false不是单身
        boolean flag = true;

        //输出变量
        System.out.println(name);
        System.out.println(age);
        System.out.println(gender);
        System.out.println(height);
        System.out.println(flag);
    }
}
