package Java的基础概念;

public class 自增自减法 {
    public static void main(String[] args) {
        //++ 和 -- 基本用法
        int a=10;
        //表示把变量a里面的值+1
        a++;
        System.out.println(a);
        //表示把变量a里面的值+1
        ++a;
        System.out.println(a);
        //表示把变量a里面的值-1
        a--;
        System.out.println(a);
        //表示把变量a里面的值
        --a;
        System.out.println(a);

    }
}
